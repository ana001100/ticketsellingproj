     private void updateSeatSelection(int seatPrice) {
        //lbselectseatpanel
        int a = 2000;
        int onfee = 100;
        String aa = Integer.toString(a);
        lbamounttf.setText(aa);
        seatCount++; // Increment seat count
        totalAmount += seatPrice; // Add seat price to total amount
        // Update text fields (ensure these text fields exist in your GUI)
        lbnumseatstf.setText(String.valueOf(seatCount));
        lbtotalamounttf.setText(String.valueOf(totalAmount));
        
        //lbordersummarypanel
        
        int kk = seatCount;
        int ll = onfee;
        int bb = seatCount * onfee;
        int b = totalAmount + bb;
        int bbb = seatPrice;
        
        String ki = Integer.toString(kk);
        String ko = Integer.toString(ll);
        String ok = (ki + " x "+ ko);
        String okey = (ki + " x " + bbb);
        
        lbseatordertf.setText(String.valueOf(okey));
        lbseattotaltf.setText(String.valueOf(totalAmount));
        olfeetf.setText(ok);
        totalolfeetf.setText(String.valueOf(bb));
        ttpricetf.setText(String.valueOf(b));
        
        //lbpurchase
    }
        
